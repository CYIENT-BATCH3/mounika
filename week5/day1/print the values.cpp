//WAP TO READ AND PRINT VALUES WITH C++
#include<iostream>
using namespace std; //scope resolution
int main()
{
	int a,b; //declaring variables
	cout<<"ENTER THE VALUES\n";
	cin>>a;   //reading from the keyboard
	cin>>b;
	cout<<"value of a is"<<a;//printing the values
	cout<<"\nvalue of b is"<<b;


	return 0;
}


